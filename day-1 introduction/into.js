// js is not statically typed language , its is loosely typed language
// it is not statically typed language because we dont have to declare the data types while declaring the variables
// it is dynamically typed language because during the runtime it will identify the data types and allocate the memory accordingly
// difference between statically typed language and dynamically typed language : when we declare the variable we have to declare the data types along with the variable name in statically typed language but in dynamically typed language we dont have to declare the data types while declaring the variables

